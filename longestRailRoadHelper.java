public class longestRailRoadHelper {
    private int size;
    private Player plr;
    public longestRailRoadHelper(int size, Player plr) {
        this.size = size;
        this.plr = plr;
    }

    public int getSize() {
        return size;
    }

    public Player getPlr() {
        return plr;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
